            car_name = kwargs.pop('car_name', None)
            super(ReportsModelForm, self).__init__(*args, **kwargs)
            if car_name and car_name in CAR_PARTS:
                self.fields['defect_part'].choices = [(part, part) for part in CAR_PARTS[car_name]]
            else:
                self.fields['defect_part'].choices = []


